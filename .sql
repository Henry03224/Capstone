-- BLOTTERS_SEQ
DECLARE
   v_max NUMBER;
BEGIN
   SELECT NVL(MAX(ID),0) INTO v_max FROM HENRY.BLOTTERS;
   EXECUTE IMMEDIATE 'ALTER SEQUENCE HENRY.BLOTTERS_SEQ INCREMENT BY ' || (v_max+1 - HENRY.BLOTTERS_SEQ.NEXTVAL);
   SELECT HENRY.BLOTTERS_SEQ.NEXTVAL INTO v_max FROM dual;
   EXECUTE IMMEDIATE 'ALTER SEQUENCE HENRY.BLOTTERS_SEQ INCREMENT BY 1';
END;
